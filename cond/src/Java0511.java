// Java0511.java
// This program demonstrates the precondition <while> loop.
// This loop will continue until the loop condition is false.


public class Java0511
{
	public static void main(String[] args)
	{   
		int number = 0;
		while (number <= 10)
		{
         System.out.println("Number: " + number);
         number++;
		}
	}
}
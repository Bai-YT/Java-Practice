// Java0509.java
// This program demonstrates how to use block structure
// with a <for> loop control structure.


public class Java0509
{
	public static void main(String[] args)
	{
		int k;
		for (k = 1; k <= 5; k++)
		{
			System.out.println("####################");
			System.out.println("Line Number " + k);
		}
	}
}

